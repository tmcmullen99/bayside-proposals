function buildWelcomeEmailText({ name, email, initialPassword, signinUrl, designerName }) {
  return [
    'Hi ' + name + ',',
    '',
    designerName + ' just set up your Bayside Pavers account. You can sign in now to explore',
    'product references and installation guides — your custom proposal will arrive in the',
    'same place when it\'s ready.',
    '',
    'Your sign-in:',
    '  Email:    ' + email,
    '  Password: ' + initialPassword,
    '',
    'That password is your property\'s street address (lowercased, no spaces). We\'ll',
    'prompt you to set your own password the first time you sign in.',
    '',
    'Sign in: ' + signinUrl,
    '',
    '— Bayside Pavers · Creating backyards people love',
  ].join('\n');
}

function escapeHtml(s) {
  return String(s == null ? '' : s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
